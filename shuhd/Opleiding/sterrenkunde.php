        <h2>Sterrenkunde Opleiding</h2>
        <p>Sterrenkunde, ook wel astronomie genoemd, is de wetenschap die zich bezighoudt met de studie van hemellichamen zoals sterren, planeten, kometen en sterrenstelsels. Een opleiding in sterrenkunde biedt studenten een uitgebreide kennis van de fysische processen en de wiskundige principes die ten grondslag liggen aan deze verschijnselen.</p>
        
        <h2>Onderwerpen die aan bod komen in een sterrenkunde opleiding:</h2>
        <ul>
            <li><strong>Basisprincipes van de astronomie:</strong> Inleiding tot sterrenkunde, geschiedenis van de astronomie, en basisbegrippen.</li>
            <li><strong>Wiskunde en natuurkunde:</strong> Calculus, lineaire algebra, differentiaalvergelijkingen, klassieke mechanica, elektromagnetisme, kwantummechanica en relativiteitstheorie.</li>
            <li><strong>Observatietechnieken:</strong> Gebruik van telescopen, spectroscopie, fotometrie en radiotelescopie.</li>
            <li><strong>Stellaire astrofysica:</strong> Bestudering van de levenscyclus van sterren, inclusief nucleosynthese, supernovae, en zwarte gaten.</li>
            <li><strong>Kosmologie:</strong> Structuur en evolutie van het universum, de oerknaltheorie en donkere materie.</li>
            <li><strong>Ruimtevaart en technologie:</strong> Satellieten, ruimtevaartmissies en instrumentatie.</li>
        </ul>

        <h2>Carrièrekansen:</h2>
        <ul>
            <li><strong>Onderzoeker:</strong> Werken bij universiteiten, onderzoeksinstituten of observatoria.</li>
            <li><strong>Docent:</strong> Lesgeven op middelbare scholen of universiteiten.</li>
            <li><strong>Ruimtevaartsector:</strong> Werken voor ruimtevaartorganisaties zoals ESA of NASA.</li>
            <li><strong>Data-analyse en IT:</strong> Analyseren van astronomische gegevens of werken in de technologie- en informatietechnologiesector.</li>
        </ul>

        <h2>Voorbeelden van universiteiten die sterrenkunde aanbieden:</h2>
        <ul>
            <li>Universiteit Leiden</li>
            <li>Universiteit van Amsterdam</li>
            <li>Rijksuniversiteit Groningen</li>
        </ul>
